import pandas as pd

# list
my_list = [1,2,3,4,5]

print(type(my_list))

# tuple
my_tuple = (1,2,3,4,5)

print(type(my_tuple))

# dict
my_dict = {'fruits':'apple', 'name':'Alyne'}

print(type(my_dict))

# set
my_set = {1,2,3,4,5}

print(type(my_set))

# array
my_array = pd.array([1,2,3,4,5])

print(type(my_array))

