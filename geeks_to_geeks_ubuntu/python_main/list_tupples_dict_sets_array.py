import pandas as pd

# list
my_list = [1,2,3,4,5]
print(my_list)
print(type(my_list))

# tuples
my_tuples = (1,2,3,4,5)
print(my_tuples)
print(type(my_tuples))

# dict
my_dict = {'a':1, 'b':2, 'c':3}
print(my_dict)
print(type(my_dict))

# sets
my_set = {1,2,3,4,5}
print(my_set)
print(type(my_set))

# array
my_array = pd.array([1,2,3,4,5])
print(my_array)
print(type(my_array))



