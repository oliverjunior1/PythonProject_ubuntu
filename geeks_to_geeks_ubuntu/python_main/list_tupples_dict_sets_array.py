import pandas as pd
# list
print('\nlist')
my_list = [1,2,3,4,5]

print(my_list)
print(type(my_list))

# tuple
print('\ntuple')
my_tuple = (1,2,3,4,5)

print(my_tuple)
print(type(my_tuple))

# dict
print('\ndict')
my_dict = {'apples':10, 'oranges':25, 'melons':15}

print(my_dict)
print(type(my_dict))

# set
print('\nset')
my_set = {1,2,3,4,5}

print(my_set)
print(type(my_set))

# array
print('\narray')
my_array = pd.array([1,2,3,4,5])

print(my_array)
print(type(my_array))




